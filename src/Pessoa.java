public class Pessoa {

    String nome;
    String sobrenome;
    int idade;
    String genero;
    String estadoCivil;

    // Construtor
    public Pessoa (String nome, String sobrenome, int idade, String genero, String estadoCivil) {
        this.nome = nome;
        this.sobrenome = sobrenome;
        this.idade = idade;
        this.genero = genero;
        this.estadoCivil = estadoCivil;
    }


}
